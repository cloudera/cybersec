package com.cloudera.parserchains.queryservice.model.sample;

import java.util.Objects;

/**
 * Describes a parser source sample that's used to store and retrieve
 * samples for Live View from the storage
 */
public class ParserSample {

    private Long id;

    private String name;

    private String description;

    private String source;

    private Boolean expectedFailure;

    private String expectedResult;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getSource() {
        return source;
    }

    public void setSource(String source) {
        this.source = source;
    }

    public Boolean getExpectedFailure() {
        return expectedFailure;
    }

    public void setExpectedFailure(Boolean expectedFailure) {
        this.expectedFailure = expectedFailure;
    }

    public String getExpectedResult() {
        return expectedResult;
    }

    public void setExpectedResult(String expectedResult) {
        this.expectedResult = expectedResult;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }
        ParserSample that = (ParserSample) o;
        return Objects.equals(id, that.id) &&
                Objects.equals(name, that.name);
    }

    @Override
    public int hashCode() {
        return Objects.hash(id, name);
    }

    @Override
    public String toString() {
        return "ParserSample{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", description='" + description + '\'' +
                ", source='" + source + '\'' +
                ", expectedResult='" + expectedResult + '\'' +
                '}';
    }
}
